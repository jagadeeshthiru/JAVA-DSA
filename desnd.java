class Descend
{
    public static void main(String[]args)
    {
        int end =20,  start=1,temp=1;
        while(start<=end)//6<=5
        {
             if(end>=11)//20>=11
             {
                System.out.println(end);//20
                
             }
             if(end<=10)//10<=10
              {
                     System.out.println(temp);//5
                     temp++;//6
                      
              }
              end--;//5
            
        }
            
     }
            
}